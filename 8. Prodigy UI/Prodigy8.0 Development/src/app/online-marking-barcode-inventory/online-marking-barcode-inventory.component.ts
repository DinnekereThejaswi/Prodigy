import { Component, OnInit } from '@angular/core';

@Component({
  selector: 'app-online-marking-barcode-inventory',
  templateUrl: './online-marking-barcode-inventory.component.html',
  styleUrls: ['./online-marking-barcode-inventory.component.css']
})
export class OnlineMarkingBarcodeInventoryComponent implements OnInit {

  EnableBarcodeUpdate: boolean = true;
  EnableCoinsUpdate: boolean = true;

  constructor() { }

  ngOnInit() {
  }

  ToggleBarcodeUpdate() {
    this.EnableBarcodeUpdate = !this.EnableBarcodeUpdate;
  }

  ToggleCoinsUpdate() {
    this.EnableCoinsUpdate = !this.EnableCoinsUpdate;
  }

}
